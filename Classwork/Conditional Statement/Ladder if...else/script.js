// Question 1 :

let mark = Number(prompt("Enter your marks : "));

if(100<=mark){
    alert("Your mark is invalid..!")
}else if(90<=mark){
    alert("Your grade is A...!")
}else if(80<=mark){
    alert("Your grade is B...!")
}else if(70<=mark){
    alert("Your grade is C...!")
}else if(60<=mark){
    alert("Your grade is D...!")
}else{
    alert("You are failed...!")
}


